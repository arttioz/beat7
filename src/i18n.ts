const LANG_KEY = 'beat7-lang';
let current: Lang = detectLang();

function detectLang(): Lang {
  try {
    const saved = localStorage.getItem(LANG_KEY);
    if (saved && STRINGS[saved as Lang]) return saved as Lang;
  } catch { /* private mode */ }
  const nav = (navigator.language || 'en').toLowerCase();
  for (const { code } of LANGS) {
    if (nav.startsWith(code)) return code;
  }
  return 'en';
}

export function getLang(): Lang {
  return current;
}

export function setLang(lang: Lang): void {
  current = STRINGS[lang] ? lang : 'en';
  try { localStorage.setItem(LANG_KEY, current); } catch { /* private mode */ }
  document.documentElement.lang = current;
}

export function t(key: string, params?: Record<string, string | number>): string {
  let s = STRINGS[current][key] ?? en[key] ?? key;
  if (params) {
    for (const [k, v] of Object.entries(params)) s = s.replaceAll(`{${k}}`, String(v));
  }
  return s;
}

/** Translate every element tagged with data-i18n (values are trusted, our own strings). */
export function applyTranslations(): void {
  document.documentElement.lang = current;
  document.querySelectorAll<HTMLElement>('[data-i18n]').forEach((el) => {
    const key = el.dataset.i18n!;
    el.innerHTML = t(key);
  });
}
